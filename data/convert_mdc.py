    with open(os.path.join(RAW_DIR, "philologeg_corpus.json"), encoding="utf-8") as f:
        data = json.load(f)
    
    print(f"البيانات الخام: {len(data)}")
    
    converted = []
    skipped = 0
    
    for item in data:
        mdc = item.get("hieroglyphic_mdc", "")
        trans = item.get("transliteration", "")
        
        unicode_hiero = mdc_to_unicode(mdc)
        
        if unicode_hiero and trans:
            converted.append({
                "hieroglyphs": unicode_hiero,
                "transliteration": trans,
                "source": item.get("source", "")
            })
        else:
            skipped += 1
    
    print(f"تم التحويل: {len(converted)}")
    print(f"تم تخطي:   {skipped}")
    
    # أمثلة
    print("\n=== أمثلة ===")
    for item in converted[:5]:
        print(f"MdC → Unicode: {item['hieroglyphs']}")
        print(f"النطق: {item['transliteration']}")
        print("---")
    
    # حفظ
    os.makedirs(PROCESSED_DIR, exist_ok=True)
