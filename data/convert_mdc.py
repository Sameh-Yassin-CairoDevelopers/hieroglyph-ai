def mdc_to_unicode(mdc_text):
    """تحويل MdC notation لرموز Unicode"""
    if not mdc_text:
        return ""
    
    # تنظيف النص
    text = mdc_text.strip()
    
    # إزالة الرموز غير المرغوبة
    text = re.sub(r'\^[a-zA-Z]\^', '', text)  # ^b^
    text = re.sub(r'<[^>]*>', '', text)         # <...>
    text = re.sub(r'\([^)]*\)', '', text)       # (...)
    
    # تقسيم على المحددات
    signs = re.split(r'[-:*!#+]', text)
    
    result = ""
    for sign in signs:
        sign = sign.strip()
        if sign in GARDINER_TO_UNICODE:
            result += GARDINER_TO_UNICODE[sign]
    
    return result

